package main

import (
	"fmt"
	"os"
	"path"

	"github.com/kong/kubernetes-testing-framework/pkg/utils/kubernetes/kubectl"

	"github.com/kong/kong-operator/v2/test/helpers/kcfg"
)

type generateValidatingPolicyParam struct {
	validatingPoliciesSubPath     string
	chartValidatingPolicyFileName string
	helmValueName                 string
}

// Generates CRDs for Helm Chart charts/kong-operator/templates/validating-policy-dataplane.yaml.
func main() {
	const autoGenerationComment = "# This file is auto-generated by KO's hack/generators/validating-policy/main.go generator.\n"
	const chartsTemplatePathPrefix = "charts/kong-operator/templates/"

	var validatingPolicyGroups = []generateValidatingPolicyParam{
		{
			validatingPoliciesSubPath:     "dataplane",
			chartValidatingPolicyFileName: "validation-policy-dataplane.yaml",
			helmValueName:                 "dataplanePorts",
		},
		{
			validatingPoliciesSubPath:     "service",
			chartValidatingPolicyFileName: "validation-policy-service-annotations.yaml",
			helmValueName:                 "serviceAnnotations",
		},
	}
	for _, param := range validatingPolicyGroups {
		chartsValidatingPolicyTemplateFilePath := path.Join(chartsTemplatePathPrefix, param.chartValidatingPolicyFileName)
		kustomizePath := path.Join(kcfg.ValidatingPoliciesPath(), param.validatingPoliciesSubPath)

		validatingPolicyKustomize, err := kubectl.RunKustomize(kustomizePath)
		if err != nil {
			fmt.Printf("Failed to run kustomize for validating policy on %s: %v",
				kustomizePath, err)
		}

		validatingPolicyContent := string(validatingPolicyKustomize)
		validatingPolicyContent = wrapInIfEnabled(param.helmValueName, validatingPolicyContent)
		validatingPolicyContent = wrapInIfCapabilitiesExist(validatingPolicyContent)

		var fileCRDs *os.File
		fileCRDs, err = os.Create(chartsValidatingPolicyTemplateFilePath)
		if err != nil {
			fmt.Printf("Failed to create %s: %v\n", chartsValidatingPolicyTemplateFilePath, err)
			os.Exit(1)
		}

		for _, content := range []string{autoGenerationComment, validatingPolicyContent} {
			if _, err := fileCRDs.WriteString(content); err != nil {
				fmt.Printf("Failed to write to %s: %v\n", chartsValidatingPolicyTemplateFilePath, err)
				os.Exit(1)
			}
		}
		fileCRDs.Close()

		fmt.Println("Successfully finished")
	}

}

func wrapInIfEnabled(valueName string, v string) string {
	// NOTE: This values.yaml field path must match the one in
	// https://github.com/Kong/kong-operator/blob/f981b357ff58dd71ff2f5eac1330f3f1693d2734/charts/kong-operator/values.yaml#L140
	return fmt.Sprintf("{{- if .Values.global.validatingPolicies.%s.enabled }}\n%s\n{{- end -}}\n",
		valueName, v)
}

func wrapInIfCapabilitiesExist(v string) string {
	// NOTE: This values.yaml field path must match the one in
	// https://github.com/Kong/kong-operator/blob/f981b357ff58dd71ff2f5eac1330f3f1693d2734/charts/kong-operator/values.yaml#L140
	return fmt.Sprintf("{{- if and (.Capabilities.APIVersions.Has \"admissionregistration.k8s.io/v1/ValidatingAdmissionPolicy\") (.Capabilities.APIVersions.Has \"admissionregistration.k8s.io/v1/ValidatingAdmissionPolicyBinding\") -}}\n%s\n{{- end -}}\n", v)
}
